SET SERVEROUTPUT ON
DECLARE
CP NUMBER;
SP NUMBER;
BEGIN
CP:=1800;
SP:=1300;
   IF (CP=SP) THEN
     DBMS_OUTPUT.PUT_LINE('NO PROFIT NO LOSS');
   
   ELSIF (SP > CP) THEN
     DBMS_OUTPUT.PUT_LINE('PROFIT IS '||(SP-CP));
  
   ELSE
    DBMS_OUTPUT.PUT_LINE('LOSS IS '||(CP-SP));
  
   END IF;
END;
/
SET SERVEROUTPUT OFF